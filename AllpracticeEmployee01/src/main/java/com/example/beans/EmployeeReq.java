package com.example.beans;

public class EmployeeReq {
	
	private String empNm;
	private Integer empSal;
	private String empDeptNm;
	private String empBloodGp;
	private String empDesg;
	
	
	public String getEmpNm() {
		return empNm;
	}
	public void setEmpNm(String empNm) {
		this.empNm = empNm;
	}
	public Integer getEmpSal() {
		return empSal;
	}
	public void setEmpSal(Integer empSal) {
		this.empSal = empSal;
	}
	public String getEmpDeptNm() {
		return empDeptNm;
	}
	public void setEmpDeptNm(String empDeptNm) {
		this.empDeptNm = empDeptNm;
	}
	public String getEmpBloodGp() {
		return empBloodGp;
	}
	public void setEmpBloodGp(String empBloodGp) {
		this.empBloodGp = empBloodGp;
	}
	public String getEmpDesg() {
		return empDesg;
	}
	public void setEmpDesg(String empDesg) {
		this.empDesg = empDesg;
	}
	
	

}
